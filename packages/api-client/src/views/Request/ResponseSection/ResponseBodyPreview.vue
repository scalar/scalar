<script lang="ts" setup>
import type { MediaPreview } from '@/views/Request/consts'
import { ref, watch } from 'vue'

import ResponseBodyInfo from './ResponseBodyInfo.vue'

const props = defineProps<{
  src: string
  type: string
  mode?: MediaPreview
  alpha?: boolean
}>()

const error = ref(false)

watch(
  () => props.src,
  () => (error.value = false),
)
</script>
<template>
  <div
    v-if="!error && src"
    class="flex justify-center overflow-hidden rounded-b"
    :class="{ 'p-2 bg-preview': alpha }">
    <img
      v-if="mode === 'image'"
      class="max-w-full"
      :class="{ rounded: alpha }"
      :src="src"
      @error="error = true" />
    <video
      v-else-if="mode === 'video'"
      autoplay
      controls
      loop
      width="100%"
      @error="error = true">
      <source
        :src="src"
        :type="type" />
    </video>
    <audio
      v-else-if="mode === 'audio'"
      class="my-12"
      controls
      loop
      @error="error = true">
      <source
        :src="src"
        :type="type" />
    </audio>
    <object
      v-else
      class="w-full aspect-[4/3]"
      :data="src"
      :type="type"
      @error="error = true" />
  </div>
  <ResponseBodyInfo v-else>Preview unavailable</ResponseBodyInfo>
</template>
