import { mkdir, readFile, writeFile } from 'node:fs/promises'
import { dirname, relative, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

/**
 * Inline the Scalar API Reference standalone bundle as a TypeScript module.
 *
 * The Fastify integration serves the standalone JavaScript build of
 * `@scalar/api-reference`. Reading that file from disk at runtime breaks once
 * the integration is bundled into a single file by a downstream build, because
 * the asset no longer sits next to the code.
 *
 * Generating a module that exports the bundle as a string makes it part of the
 * module graph, so it survives any downstream bundling.
 *
 * @see https://github.com/scalar/scalar/issues/3566
 */
const scriptDir = dirname(fileURLToPath(import.meta.url))

const source = resolve(scriptDir, '../../../packages/api-reference/dist/browser/standalone.js')
const target = resolve(scriptDir, '../src/assets/standalone.generated.ts')

const bundle = await readFile(source, 'utf8').catch(() => {
  throw new Error(
    `Could not read ${source}. Build @scalar/api-reference first (e.g. \`pnpm --filter @scalar/api-reference build\`).`,
  )
})

await mkdir(dirname(target), { recursive: true })
await writeFile(
  target,
  [
    '// Generated by scripts/inline-standalone.js — do not edit.',
    // The explicit `string` annotation keeps the emitted declaration small,
    // instead of inlining the whole bundle as a literal type.
    `export const scalarJs: string = ${JSON.stringify(bundle)}`,
  ].join('\n') + '\n',
)

console.log(`Inlined standalone bundle → ${relative(process.cwd(), target)}`)
